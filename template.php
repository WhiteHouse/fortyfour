/**
 * Override or insert variables into the fieldable_panels_pane templates.
 *
 * @param $variables
 *   An array of variables to pass to the theme template.
 * @param $hook
 *   The name of the template being rendered ("block" in this case.)
 */
function thinskin_preprocess_fieldable_panels_pane(&$variables, $hook) {
  $theme_hook_suggestions = $variables['theme_hook_suggestions'];

  // Determine if this is a featured content fieldable panel pane.
  if (in_array('fieldable_panels_pane__feature_content_panel_pane', $theme_hook_suggestions)) {
    // Store the title to provide as an easy variable in the tpl.
    $title = $variables['elements']['#fieldable_panels_pane']->title;
    if (array_key_exists('field_featured_link', $variables)) {
      // Store the link.
      $link = $variables['field_featured_link'];

      // If this contains a link then set the title to display as a link.
      if (!empty($link) && !empty($title)) {
        $title = l($title, $link[0]['url']);
      }
    }

    $variables['title'] = filter_xss_admin($title);
  }
}
